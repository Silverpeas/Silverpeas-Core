package org.silverpeas.core.web.rs.aspect;

import org.silverpeas.core.web.rs.ProtectedWebResource;
import org.silverpeas.core.admin.component.model.ComponentInst;

import javax.interceptor.InterceptorBinding;
import java.lang.annotation.Documented;
import java.lang.annotation.Inherited;
import java.lang.annotation.Retention;
import java.lang.annotation.Target;

import static java.lang.annotation.ElementType.TYPE;
import static java.lang.annotation.RetentionPolicy.RUNTIME;

/**
 * Existence of {@link ComponentInst} is verified on each method execution of a {@link
 * ProtectedWebResource} instance which the class is annotated with this annotation.
 * @author Yohann Chastagnier
 */
@InterceptorBinding
@Documented
@Target(TYPE)
@Retention(RUNTIME)
@Inherited
public @interface ComponentInstMustExistIfSpecified {}
