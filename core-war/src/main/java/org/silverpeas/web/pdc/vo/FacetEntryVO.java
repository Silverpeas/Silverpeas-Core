package org.silverpeas.web.pdc.vo;

public class FacetEntryVO {

  private String name = "";
  private String id = "";
  private int nbElt = 0;
  private boolean selected = false;

  /**
   * Default constructor
   */
  public FacetEntryVO() {
    super();
  }

  /**
   * Constructor with fields
   * @param name the author name
   * @param id the author identifier
   */
  public FacetEntryVO(String name, String id) {
    super();
    this.name = name;
    this.id = id;
    this.nbElt = 1;
  }

  public String getName() {
    return name;
  }

  public void setName(String name) {
    this.name = name;
  }

  public String getId() {
    return id;
  }

  public void setId(String id) {
    this.id = id;
  }

  public int getNbElt() {
    return nbElt;
  }

  public void incrementEntry() {
    this.nbElt++;
  }

  @Override
  public int hashCode() {
    final int prime = 31;
    int result = 1;
    result = prime * result + ((id == null) ? 0 : id.hashCode());
    result = prime * result + ((name == null) ? 0 : name.hashCode());
    return result;
  }

  public void setSelected(boolean selected) {
    this.selected = selected;
  }

  public boolean isSelected() {
    return selected;
  }

  @Override
  public boolean equals(Object obj) {
    if (this == obj)
      return true;
    if (obj == null)
      return false;
    if (getClass() != obj.getClass())
      return false;
    FacetEntryVO other = (FacetEntryVO) obj;
    if (id == null) {
      if (other.id != null)
        return false;
    } else if (!id.equals(other.id))
      return false;
    if (name == null) {
      if (other.name != null)
        return false;
    } else if (!name.equals(other.name))
      return false;
    return true;
  }


}
